#include<stdio.h>
int main()
{
  int a,b;
  printf("Enter value of a:");
  scanf("%d", &a);
  printf("Enter a value of b:");
  scanf("%d", &b);
  printf("%d\n", a+b); //add
  printf("%d\n", a-b); //sub
  printf("%d\n", a*b); //multiply
  printf("%d\n", a/b); //divide
  printf("%d\n", a%b); //modulus
  return 0;
}

